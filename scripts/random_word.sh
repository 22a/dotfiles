#!/bin/bash

# taken from https://blogs.warwick.ac.uk/mikewillis/entry/random_words/
WORDFILE=/usr/share/dict/words
RANDOM=$$; # seed random from pid
lines=$(cat $WORDFILE  | wc -l); # using cat means wc outputs only a number, not number followed by filename
rnum=$((RANDOM*RANDOM%$lines+1));
sed -n "$rnum p" $WORDFILE;
